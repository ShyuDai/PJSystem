package com.pj.entity;

public class Power {
	private int id;
	private String power;
	private int setpower;
	private int gangweiid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPower() {
		return power;
	}
	public void setPower(String power) {
		this.power = power;
	}
	public int getSetpower() {
		return setpower;
	}
	public void setSetpower(int setpower) {
		this.setpower = setpower;
	}
	public int getGangweiid() {
		return gangweiid;
	}
	public void setGangweiid(int gangweiid) {
		this.gangweiid = gangweiid;
	}
	

}
